#include "3-calc.h"
/**
 * op_add - add two number
 * @a: first input
 * @b: second input
 * Return: the value
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two number
 * @a: first input
 * @b: second input
 * Return: the value
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product two number
 * @a: first input
 * @b: second input
 * Return: the value
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of two number
 * @a: first input
 * @b: second input
 * Return: the value
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remind of division of two number
 * @a: first input
 * @b: second input
 * Return: the value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
